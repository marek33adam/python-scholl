#Názvy proměnných s více než jedním slovem mohou být obtížně čitelné.
#Existuje několik technik, které můžete použít, aby byly čitelnější

#Camel Case:
#Každé slovo, kromě prvního, začíná velkým písmenem
myVariableName = "John"

#Pascal Case:
#Každé slovo začíná velkým písmenem:
MyVariableName = "John"

#Snake Case
#Každé slovo je odděleno znakem podtržítka:
my_variable_name = "John"
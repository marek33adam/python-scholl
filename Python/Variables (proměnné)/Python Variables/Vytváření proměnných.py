#Proměnné jsou kontejnery pro ukládání datových hodnot.
#Python nemá žádný příkaz pro deklaraci proměnné. Proměnná se vytvoří v okamžiku, kdy jí poprvé přiřadíte hodnotu.
# Příklad
x = 5
y = "John"
print(x)
print(y)

#Proměnné nemusí být deklarovány žádným konkrétním typem a mohou dokonce změnit typ poté, co byly nastaveny.
#příklad
x = 4       # x je typu int
x = "Sally" # x je nyní typu str
print(x)
